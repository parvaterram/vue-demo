<template>
  <div>
    <myHeader></myHeader>
    <div class="article-list">
      <ul>
        <li v-for="i in list">
          <time v-text="$utils.formatTime(i.create_at)"></time>
          <router-link :to="'/content/' + i.id">
            {{i.title}}
          </router-link>
        </li>
      </ul>
    </div>
    <myFooter></myFooter>
  </div>
</template>
<script>
  import myHeader from '../components/header.vue'
  import myFooter from '../components/footer.vue'

  export default {
    components: {
      myHeader, myFooter
    },
    data () {
      return {
        list: []
      }
    },
    created () {
      this.getData()
    },
    methods: {
      getData () {
        this.$api.get('topics', null, res => {
          this.list = res.data
        })
      }
    }
  }
</script>
